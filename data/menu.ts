import type { MenuItem } from "@/types";

type RawMenuItem = [
  categoryJa: string,
  categoryEn: string,
  nameJa: string,
  nameEn: string,
  price?: string,
  flags?: Partial<Pick<MenuItem, "spicy" | "vegetarian" | "recommended" | "kind">>,
];

function buildItems(section: "food" | "drinks" | "lunch", rows: RawMenuItem[]): MenuItem[] {
  return rows.map(([categoryJa, categoryEn, nameJa, nameEn, price, flags], index) => ({
    id: `${section}-${String(index + 1).padStart(3, "0")}`,
    sourceOrder: index + 1,
    section,
    categoryJa,
    categoryEn,
    nameJa,
    nameEn,
    price,
    enabled: true,
    ...flags,
  }));
}

